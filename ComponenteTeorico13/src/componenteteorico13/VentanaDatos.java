    private void btnDeleteColumnaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteColumnaActionPerformed
        // TODO add your handling code here:
        // Eliminar la ultima columna de la tabla
        // Obtener el modelo de la tabla
        DefaultTableModel modeloTabla = (DefaultTableModel)tablaValores.getModel();
        int cantidadColumnas = modeloTabla.getColumnCount();
        modeloTabla.setColumnCount(cantidadColumnas - 1);
        // Actualizar Tabla
        tablaValores.setModel(modeloTabla);
    }//GEN-LAST:event_btnDeleteColumnaActionPerformed

    private void btnDeleteFilaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteFilaActionPerformed
        // TODO add your handling code here:
        // Obtener el modelo de la tabla
        DefaultTableModel modeloTabla = (DefaultTableModel)tablaValores.getModel();
        int cantidadFilas = modeloTabla.getRowCount();
        // Eliminar ultima fila de la tabla
        modeloTabla.removeRow(cantidadFilas - 1);
        // Actualizar modelo
        tablaValores.setModel(modeloTabla);
    }//GEN-LAST:event_btnDeleteFilaActionPerformed

