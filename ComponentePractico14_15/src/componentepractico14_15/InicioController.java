    private void btnCrearProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearProductoActionPerformed
        // TODO add your handling code here:
        Connection conexionBD = conector.crearConexion();
        if(conexionBD == null) {
            JOptionPane.showMessageDialog(this, "No se pudo crear la conexion");
        } else {
            try {
                // Extraer información de los campos de texto
                String codigo = txtCodigo.getText();
                String nombre = txtDescripcion.getText();
                int precioCompra = Integer.parseInt(txtPrecioCompra.getText());
                int precioVenta = Integer.parseInt(txtPrecioVenta.getText());
                int cantidadBodega = Integer.parseInt(txtCantidadBodega.getText());
                int cantidadMinima = Integer.parseInt(txtCantidadMinima.getText());
                int cantidadMaxima = Integer.parseInt(txtCantidadMaxima.getText());
                
                String textoQuery = "INSERT INTO PRODUCTOS (CODIGO,NOMBRE,PRECIO_COMPRA,PRECIO_VENTA,CANTIDAD_BODEGA,CANTIDAD_MINIMA,CANTIDAD_MAXIMA) VALUES ('%1$s', '%2$s', %3$d, %4$d, %5$d, %6$d, %7$d)";
                String query = String.format(textoQuery, codigo, nombre, precioCompra, precioVenta, cantidadBodega, cantidadMinima, cantidadMaxima);
                
                int filasAfectadas = conector.ejecutarActualizacion(conexionBD, query);
                JOptionPane.showMessageDialog(this, String.format("Producto creado exitosamente. Filas afectadas: %1$d", filasAfectadas));
            } catch (NumberFormatException ex) {
                JOptionPane.showMessageDialog(this, "Falló la conversión de datos");
                System.out.println(ex.getMessage());
            }
            
        }
    }//GEN-LAST:event_btnCrearProductoActionPerformed

