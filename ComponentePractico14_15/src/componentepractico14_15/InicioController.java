    private void btnMostrarProductosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarProductosActionPerformed
        // TODO add your handling code here:
        String query = "SELECT * FROM PRODUCTOS";
        // Creando la conexión a la base de datos
        Connection conexion = conector.crearConexion();
        // Guardar en un ResultSet el resultado de la consulta SQL
        ResultSet productos = conector.ejecutarQuery(conexion, query);
        String columnas = "CODIGO-NOMBRE-PRECIO COMPRA-PRECIO VENTA-CANTIDAD BODEGA-CANTIDAD MINIMA-CANTIDAD MAXIMA";
        // Limpiar lista
        limpiarListaProductos();
        agregarProductoALista(columnas);
        // Agregar resultados de la tabla en la lista
        try {
            while(productos.next()) {
                String codigo = productos.getString("CODIGO");
                String nombre = productos.getString("NOMBRE");
                int precioCompra = productos.getInt("PRECIO_COMPRA");
                int precioVenta = productos.getInt("PRECIO_VENTA");
                int cantidadBodega = productos.getInt("CANTIDAD_BODEGA");
                int cantidadMinima = productos.getInt("CANTIDAD_MINIMA");
                int cantidadMaxima = productos.getInt("CANTIDAD_MAXIMA");
                
                String linea = "%1$s-%2$s-%3$d-%4$d-%5$d-%6$d-%7$d";
                String lineaProducto = String.format(linea, codigo, nombre, precioCompra, precioVenta, cantidadBodega, cantidadMinima, cantidadMaxima);
                agregarProductoALista(lineaProducto);
            }
        } catch (SQLException ex) {
            
        }
        
    }//GEN-LAST:event_btnMostrarProductosActionPerformed

